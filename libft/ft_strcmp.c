#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;

	if (s1[0] == 0)
		return (-(unsigned char)s2[0]);
	if (s2[0] == 0)
		return ((unsigned char)s1[0]);
	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
