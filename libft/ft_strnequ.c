#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;

	if (!n)
		return (1);
	if (!s1 && !s2)
		return (1);
	if (!s1 || !s2)
		return (0);
	i = 0;
	while (n > 0)
	{
		if (s1[i] != s2[i])
			return (0);
		n--;
		i++;
	}
	return (1);
}
